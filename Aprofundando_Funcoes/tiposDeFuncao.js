//Tipos de funções

//declarativas
function funcao1(){
    console.log('Sou uma mensagem de uma função declarativa')
}
funcao1();

// expressão de função
// com nomeação
var funcao2 = function(){
    console.log('Sou uma mensagem de função de expressão')
}
funcao2();

//arrow function

var funcao3 = () => {
    console.log('Sou uma arrow function')

}
funcao3();